#include <iostream>

int main()
{
	std::cout << "Hello, ";
	std::cout << "World! ";
	std::cout << "Lorem ";
	std::cout << "Ipsum";
	std::cout << std::endl;

	std::cout << "Hello, ";
	std::cout << std::endl;
	std::cout << "World! ";
	std::cout << std::endl;
	std::cout << "Lorem ";
	std::cout << std::endl;
	std::cout << "Ipsum";
	std::cout << std::endl;
	
	std::cout << "Hello, " << "World! " << "Lorem " << "Ipsum" << std::endl;	

	std::cout << 2021;
	std::cout << " ";
	std::cout << ',';
	std::cout << " ";
	std::cout << true;
	std::cout << " ";
	std::cout << false;
	std::cout << std::endl;
	
	std::cout << 2021 << " , " << true << " " << false << std::endl;
	
	return 0;
}
